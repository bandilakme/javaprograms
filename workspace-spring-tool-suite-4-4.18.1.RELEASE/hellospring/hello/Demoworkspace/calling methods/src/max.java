
public class max  {
	//main method
	public static void main(String[]  args)  {
		int maxValue=Integer.MIN_VALUE;
		int[] arr= {2,6,8,10,12,14};  // declare an array of numbers
		for(int i=0; i<arr.length;i++)  {
			if (arr[i] > maxValue)  {
				maxValue=arr[i];
			}
			System.out.print(" The maximum element in the array is= " +maxValue);  //output max 
		}
	}
}
		
	
					
	
			
			
		
	

		
	
		
	
		
			
		
			
		
		
			
		
		
	
	


